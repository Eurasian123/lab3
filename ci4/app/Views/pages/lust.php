<h1>Honkai Impact 3rd</h1>
<p>Honkai Impact 3rd is a 3D action RPG, but also features inspirations from similar genres like hack-and-slash. The game takes place in a significantly more technologically advanced modern world, where the Earth is facing another apocalypse as it does every 50000 years. Part 1 focuses on a trio of in-training Valkyries, Kiana, Mei, and Bronya. Part 2, taking place on Mars, focuses on a new cast of characters, particularly Coralie, Helia, Dreamseeker, and Senadina.</p>

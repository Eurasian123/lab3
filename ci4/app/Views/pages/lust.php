<h1>Genshin Impact</h1>
<p>Genshin Impact is a high fantasy action RPG that takes place in the diverse world of Teyvat. The game follows the Traveler, a lost outlander who is searching for their sibling.</p>

<p>If it were up to playtime alone, many would assume that Genshin would be my favorite game at this point. While it's not wrong, it's not exactly right either.<br />Besides the point, Genshin is a great game, with responsive gameplay, a great story (not counting the first three regions), and a breathtaking world. The world design and lore is the game's strongest suit, and also my biggest interest. Just the vast array of places and biomes even respective to each region; every symbolism and inspiration thoroughly researched to make a more grandiose world. Most importantly, the music is always incredible.</p>

<h1>Honkai: Star Rail</h1>
<p>Honkai: Star Rail is a turn-based RPG, which takes place in a universe of high stellar technological advancement, where ideas are so powerful that there exists "Aeons" who are the embodiments of certain ideologies, known as "Paths." The game follows the Astral Express, who follow the Path of the Trailblaze.</p>
<p>Another surprise! While I initially disliked it due to its popularity in betas, trying for the first time, I was hooked. But also, many others were too. It didn't matter that it was a turn-based RPG, it still got massively popular because it used Genshin's formula for success. Due to this, it has very much the same qualities as Genshin with even less cons.</p>

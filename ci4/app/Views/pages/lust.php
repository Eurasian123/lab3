<h1>Lethal Company</h1>
<p>Lethal Company is a first-person co-op horror game. Set in a distant retro future, the game is about a team of contracted employees working for "The Company" to collect scrap on abandoned moons.</p>
<p>I didn't expect to fall in love with this game the way I did. While it is amazing to play with others, I've played this most often solo&mdash; usually an incredibly difficult task. I personally am not a fan of horror at all, yet Lethal Company scratches that itch and makes me brave into the unknown simply for the serotonin of collecting leftover scrap. The game is very well-designed despite being in early access, and is plenty fun to play, especially once a week. This is a game that combines diegetic comedy and incredibly effective horror all into one experience, looped over and over with each moon yet always serving a new experience.</p>
</div>
